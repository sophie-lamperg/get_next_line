#include "libft.h"

char	*ft_strcpy(char *dest, const char *s)
{
	size_t i;

	i = 0;
	while ((s[i] != '\0'))
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
